package edu.ucla.library.libservices.rss.custom.beans.tables;

public class MarcLanguageCode
{
  private String code;
  private String language;
  private String uclaDisplay;

  public MarcLanguageCode()
  {
    setCode( "" );
    setLanguage( "" );
    setUclaDisplay( "" );
  }

  public void setCode( String code )
  {
    this.code = code;
  }

  public String getCode()
  {
    return code;
  }

  public void setLanguage( String language )
  {
    this.language = language;
  }

  public String getLanguage()
  {
    return language;
  }

  public void setUclaDisplay( String uclaDisplay )
  {
    this.uclaDisplay = uclaDisplay;
  }

  public String getUclaDisplay()
  {
    return uclaDisplay;
  }
}
